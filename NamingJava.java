public class NamingJava {
    public static void main(String[] args) {
        int celphone = 822918372;
        int celPhone = 238919238;
        //Si no ponemos la variable como esta establecida
        //marcara error
        System.out.println(celPhone);
        System.out.println(celphone);

        String $countryName = "México";
        String _backgroundColor = "Blue";

        String currency$ = "MXN";
        String background_color = "Purple";

        int POSITION = -5;
        int MAX_WIDTH = 9999;
        int MIN_WIDTH = 1;

        //Lower Camel Case
        String fullName = "Jessica Melissa Villarreal";
        int sizeInCentimeters = 25;




    }
}

